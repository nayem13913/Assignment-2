 // Step 1: Create the calculateSquare arrow function

 // Step 2: Call the calculateSquare function with different numbers

// Step 3: Print the results to the console




 let calculateSquare = (number) =>{
    return number * number;
 }

 let num1 = 5;
 let num2 = 8;
 let num3 = 12;

let result1 = calculateSquare(num1);
let result2 = calculateSquare(num2);
let result3 = calculateSquare(num3);

 console.log(result1); // output: 25

 console.log(result2); //output: 64

 console.log(result3); // output: 144